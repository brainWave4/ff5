.export ExecBtlGfx_ext

; ---------------------------------------------------------------------------

.segment "battle_gfx_code"

.a8
.i16

; ---------------------------------------------------------------------------

.proc ExecBtlGfx_ext
_0000:  jmp     ExecBtlGfx
.endproc

; ---------------------------------------------------------------------------

.proc _c10003
_0003:  jmp     $f75f
.endproc

; ---------------------------------------------------------------------------

.proc _c10006
_0006:  jmp     $e999
.endproc

; ---------------------------------------------------------------------------

.proc _c10009
_0009:  jmp     _c1003f
.endproc

; ---------------------------------------------------------------------------

; [ execute graphics function ]

.proc ExecBtlGfx
@000c:  asl
        tax
        lda     f:ExecBtlGfxTbl,x
        sta     $7a
        lda     f:ExecBtlGfxTbl+1,x
        sta     $7b
        jsr     @001e
        rtl
@001e:  jmp     ($007a)
.endproc  ; ExecBtlGfx

; ---------------------------------------------------------------------------

ExecBtlGfxTbl:
@0021:  .word $39f9, $3a55, $0348, $02e0, $02df, $422b, $01e4, $768d
        .word $75d7, $0208, $8189, $010b, $00f2, $00c6, $0092

